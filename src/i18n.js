/** @returns {Locale} */
export function getLocale() {
  const pathOnly = location.pathname.replace(/\/index\.html$/i, "");
  const segments = pathOnly.split("/").filter(Boolean);
  const lastSeg = segments[segments.length - 1]?.toLowerCase();
  if (lastSeg === "de") return "de";
  if (lastSeg === "en") return "en";
  const q = new URLSearchParams(location.search).get("lang");
  if (q === "de") return "de";
  if (q === "en") return "en";
  return "da";
}

let activeLang = "da";

/** Bundne strenge for aktivt sprog (defaults + evt. overrides fra content/overrides.json). */
let currentTable = /** @type {Record<string, string>} */ ({ ...STRINGS.da });

/** @param {string} key */
export function t(key) {
  return currentTable[key] ?? STRINGS.da[key] ?? key;
}

/**
 * @param {Partial<Record<Locale, Record<string, string>>> | null} localePatches
 * Valgfrie felter pr. sprog — samme nøgler som i STRINGS (fx introTagline).
 */
export function applyI18n(localePatches = null) {
  activeLang = getLocale();
  const lang = activeLang;
  document.documentElement.lang = lang;

  const defaults = STRINGS[lang] ?? STRINGS.da;
  const patch = localePatches?.[lang] ?? {};
  currentTable = { ...defaults, ...patch };
  const table = currentTable;
  document.title = table.pageTitle;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", table.metaDescription);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!key || !(key in table)) return;
    el.textContent = /** @type {any} */ (table)[key];
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (!key || !(key in table)) return;
    el.innerHTML = /** @type {any} */ (table)[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (!key || !(key in table) || !("placeholder" in el)) return;
    el.placeholder = /** @type {any} */ (table)[key];
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria-label");
    if (!key || !(key in table)) return;
    el.setAttribute("aria-label", /** @type {any} */ (table)[key]);
  });

  const base = import.meta.env.BASE_URL || "./";
  const baseNorm = base.endsWith("/") ? base : `${base}/`;
  const homeDa = `${baseNorm}`;
  const homeDe = `${baseNorm}de/`;
  const homeEn = `${baseNorm}en/`;

  const brand = document.querySelector(".brand");
  if (brand) {
    if (lang === "de") brand.setAttribute("href", homeDe);
    else if (lang === "en") brand.setAttribute("href", homeEn);
    else brand.setAttribute("href", homeDa);
  }

  const langDa = document.querySelector('[data-lang-link="da"]');
  const langDe = document.querySelector('[data-lang-link="de"]');
  const langEn = document.querySelector('[data-lang-link="en"]');
  if (langDa) {
    langDa.setAttribute("href", homeDa);
    langDa.classList.toggle("lang-link--default", lang === "da");
  }
  if (langDe) {
    langDe.setAttribute("href", homeDe);
    langDe.classList.toggle("lang-link--default", lang === "de");
  }
  if (langEn) {
    langEn.setAttribute("href", homeEn);
    langEn.classList.toggle("lang-link--default", lang === "en");
  }

  const lead = document.querySelector(".intro .lead");
  const introTagline = document.querySelector(".intro .intro-tagline");
  if (lead) lead.setAttribute("lang", lang);
  if (introTagline) introTagline.setAttribute("lang", lang);
}
